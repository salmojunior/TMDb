import UIKit

/// Custom app colors
extension UIColor {
    class var tmdbBlack: UIColor {
        return UIColor(red: 8/255, green: 28/255, blue: 36/255, alpha: 1.0)
    }
    
    class var tmdbGreen: UIColor {
        return UIColor(red: 1/255, green: 210/255, blue: 119/255, alpha: 1.0)
    }
}
